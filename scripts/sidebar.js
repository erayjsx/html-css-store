function openNav() {
    document.getElementById("myNav").style.width = "90%";
    document.querySelector("body").style.overflow = "hidden";
}

function closeNav() {
    document.getElementById("myNav").style.width = "0%";
    document.querySelector("body").style.overflow = "auto";
}

